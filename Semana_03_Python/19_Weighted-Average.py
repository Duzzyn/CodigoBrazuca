num1 = int(input("Informe um número para a média ponderada: "))
num2 = int(input("Informe um segundo número para a média ponderada: "))
num3 = int(input("Informe um terceiro número para a média ponderada: "))

multi = (num1 * 2)+(num2 * 3)+(num3 * 5)
soma = 2 + 3 + 5

resultado = multi / soma

print(f"A média ponderada das três notas é {resultado}")